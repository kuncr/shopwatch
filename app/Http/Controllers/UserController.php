    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $listRole = Role::all();
        $user = User::find($id);
        return view('user.edit',compact('user','listRole'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(UserRequest $request, $id)
    {
        $user = User::find($id);
        $data = $request->except('_token','_method');
        $user->update($data);
        return redirect()->route('list-user')->with(['flash_level'=>'success','flash_message'=>'Cập nhật thành công']);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        User::destroy($id);
        return redirect()->route('list-user')->with(['flash_level'=>'success','flash_message'=>'Xóa thành công']);
    }
